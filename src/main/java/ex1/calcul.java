package ex1;

/**
 * Cette classe permet de faire des opérations mathématiques.
 */
public class Calcul {

	/**
	 * Cette méthode additionne 2 entiers et retourne le résultat.
	 * 
	 * @param a Premier entier
	 * @param b Second entier
	 * @return Somme des 2 entiers
	 */
	public int add(int a, int b) {
		return a + b;
	}

	/**
	 * Cette méthode soustraits 2 entiers et retourne le résultat.
	 * 
	 * @param a Premier entier
	 * @param b Second entier
	 * @return Soustraction des 2 entiers
	 * */
	public int substract(int a, int b) {
		return a - b;
	}
}
